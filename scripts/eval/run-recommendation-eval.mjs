// scripts/eval/run-recommendation-eval.mjs
/**
 * Offline recommendation-evaluation runner (Phase F8A).
 *
 * SAFE BY CONSTRUCTION: imports only the pure metric module + SYNTHETIC
 * fixtures. No DB, no network, no engine mutation, no auth. Run any time:
 *
 *   node scripts/eval/run-recommendation-eval.mjs
 *
 * Writes a baseline report to docs/eval/:
 *   - recommendation-eval-baseline.json  (machine-readable)
 *   - recommendation-eval-baseline.md    (human-readable)
 *
 * To evaluate REAL data later (F8B), export a read-only snapshot into the same
 * record shapes (see docs/sql/recommendation-evaluation-queries.sql) and pass
 * it to summarizeEvaluation — the metric code does not change.
 */

import { writeFileSync, mkdirSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, join } from 'node:path'

import {
  summarizeEvaluation,
  scoreExplanation,
} from '../../src/shared/services/eval/recommendationEval.js'
import { SAMPLE_DATASET, SAMPLE_REASONS } from '../../src/shared/services/eval/fixtures.js'

const here = dirname(fileURLToPath(import.meta.url))
const outDir = join(here, '..', '..', 'docs', 'eval')

const report = summarizeEvaluation(SAMPLE_DATASET)
const reasonBreakdown = SAMPLE_REASONS.map((r) => ({ text: r.text, ...scoreExplanation(r) }))

// --- write JSON -------------------------------------------------------------
mkdirSync(outDir, { recursive: true })
const jsonPath = join(outDir, 'recommendation-eval-baseline.json')
writeFileSync(jsonPath, JSON.stringify({ report, reasonBreakdown }, null, 2) + '\n')

// --- write Markdown ---------------------------------------------------------
const md = renderMarkdown(report, reasonBreakdown)
const mdPath = join(outDir, 'recommendation-eval-baseline.md')
writeFileSync(mdPath, md)

// --- console summary --------------------------------------------------------
/* eslint-disable no-console */
console.log('FeelFlick — recommendation evaluation baseline (fixture-based)')
console.log('  fit (outcomes):       ', JSON.stringify(report.fitQuality))
console.log('  repeated-pick fatigue:', JSON.stringify(report.repeatedPickFatigue))
console.log('  diversity:            ', JSON.stringify(report.diversity))
console.log('  language mix:         ', JSON.stringify(report.languageMix))
console.log('  reason coverage:      ', JSON.stringify(report.reasonCoverage))
console.log('  explanation quality:  ', JSON.stringify(report.explanationQuality))
console.log('  cold/warm:            ', JSON.stringify(report.coldWarm))
console.log(`\nWrote: ${jsonPath}`)
console.log(`Wrote: ${mdPath}`)
/* eslint-enable no-console */

/**
 * @param {object} report
 * @param {Array<object>} reasonBreakdown
 * @returns {string}
 */
function renderMarkdown(report, reasonBreakdown) {
  const f = report.fitQuality
  const r = report.repeatedPickFatigue
  const d = report.diversity
  const l = report.languageMix
  const rc = report.reasonCoverage
  const eq = report.explanationQuality
  const cw = report.coldWarm
  const lines = []
  lines.push('# Recommendation evaluation — baseline (fixture-based)')
  lines.push('')
  lines.push('> **Generated by** `scripts/eval/run-recommendation-eval.mjs` from SYNTHETIC')
  lines.push('> fixtures — not production data. Regenerate with')
  lines.push('> `node scripts/eval/run-recommendation-eval.mjs`. This file is a worked')
  lines.push('> example of the metrics; the framework + real-data baseline live in')
  lines.push('> [`recommendation-trust-evaluation-f8a.md`](../recommendation-trust-evaluation-f8a.md).')
  lines.push('')
  lines.push(`_Generated: ${report.generatedAt}_`)
  lines.push('')
  lines.push('## Fit quality (outcome rates)')
  lines.push('')
  lines.push('| metric | value |')
  lines.push('|---|---|')
  lines.push(`| impressions | ${f.total} |`)
  lines.push(`| skip rate | ${f.skipRate} |`)
  lines.push(`| click rate | ${f.clickRate} |`)
  lines.push(`| watch rate | ${f.watchRate} |`)
  lines.push(`| save rate | ${f.saveRate} |`)
  lines.push(`| **outcome-capture rate** | **${f.outcomeCaptureRate}** |`)
  lines.push('')
  lines.push('## Repeated-pick fatigue (hero)')
  lines.push('')
  lines.push('| metric | value |')
  lines.push('|---|---|')
  lines.push(`| shown | ${r.totalShown} |`)
  lines.push(`| distinct movies | ${r.distinctMovies} |`)
  lines.push(`| distinct ratio | ${r.distinctRatio} |`)
  lines.push(`| consecutive-repeat rate | ${r.consecutiveRepeatRate} |`)
  lines.push(`| max streak | ${r.maxStreak} |`)
  lines.push('')
  lines.push('## Diversity & language (sample result set)')
  lines.push('')
  lines.push('| metric | value |')
  lines.push('|---|---|')
  lines.push(`| director diversity | ${d.directorDiversity} |`)
  lines.push(`| genre diversity | ${d.genreDiversity} |`)
  lines.push(`| decade diversity | ${d.decadeDiversity} |`)
  lines.push(`| composite | ${d.composite} |`)
  lines.push(`| distinct languages | ${l.distinctLanguages} |`)
  lines.push(`| dominant-language share | ${l.dominantShare} |`)
  lines.push(`| non-English share | ${l.nonEnglishShare} |`)
  lines.push('')
  lines.push('## Reason coverage')
  lines.push('')
  lines.push('| metric | value |')
  lines.push('|---|---|')
  lines.push(`| grounded share | ${rc.groundedShare} |`)
  lines.push(`| generic share | ${rc.genericShare} |`)
  lines.push(`| seed share | ${rc.seedShare} |`)
  lines.push(`| distinct reason types | ${rc.distinctReasonTypes} |`)
  lines.push('')
  lines.push('## Explanation quality')
  lines.push('')
  lines.push(`Mean score **${eq.meanScore}** · good ${eq.byVerdict.good} · weak ${eq.byVerdict.weak} · generic ${eq.byVerdict.generic} · **unsafe ${eq.byVerdict.unsafe}**`)
  lines.push('')
  lines.push('| explanation | verdict | score | safe |')
  lines.push('|---|---|---|---|')
  for (const x of reasonBreakdown) {
    lines.push(`| ${x.text} | ${x.verdict} | ${x.score} | ${x.safe ? '✓' : '✗ FAIL'} |`)
  }
  lines.push('')
  lines.push('## Cold / warm segmentation')
  lines.push('')
  lines.push(`cold ${cw.cold} · warming ${cw.warming} · warm ${cw.warm} (of ${cw.total})`)
  lines.push('')
  return lines.join('\n')
}
